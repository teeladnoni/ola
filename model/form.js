import mongoose from "mongoose";

const FormSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    phone: {
        type: Number,
        required: true
    },
    password: {
        type: String,
        required: true
    }
});

const Forms = mongoose.model("Form", FormSchema);

export default Forms;   
